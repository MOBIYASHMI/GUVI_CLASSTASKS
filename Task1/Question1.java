package GUVI_CLASSTASKS.Task1;
import java.util.Scanner;
public class Question1 {
    public static void main(String[] args) {
        // for-loop
        System.out.println("for loop");
        for(int i=10;i<=50;i++){
            System.out.print(i+" ");
        }
        System.out.println();
        // while loop
        System.out.println("while loop");
        int a=10;
        while(a<=50){
            System.out.print(a+" ");
            a++;
        }
    }
}
